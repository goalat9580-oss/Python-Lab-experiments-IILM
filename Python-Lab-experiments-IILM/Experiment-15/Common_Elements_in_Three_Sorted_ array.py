class Solution:
    def commonElements(self, arr1, arr2, arr3):
        i = 0
        j = 0
        k = 0

        result = []

        n1 = len(arr1)
        n2 = len(arr2)
        n3 = len(arr3)

        while i < n1 and j < n2 and k < n3:
            if arr1[i] == arr2[j] and arr2[j] == arr3[k]:
                if not result or result[-1] != arr1[i]:
                    result.append(arr1[i])
                i += 1
                j += 1
                k += 1
            elif arr1[i] < arr2[j]:
                i += 1
            elif arr2[j] < arr3[k]:
                j += 1
            else:
                k += 1

        return result
